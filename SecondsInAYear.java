public class SecondsInAYear {

    public static void main(String[] args) {
        int daysInAYear = 365;
        int hoursInADay = 24;
        int minutesInAnHour = 60;
        int secondsInAMinute = 60;
        System.out.println(
                daysInAYear * hoursInADay * minutesInAnHour * secondsInAMinute
        );
    }
}
